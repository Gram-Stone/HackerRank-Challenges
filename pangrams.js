function pangrams(s) {
  const alphabet = [
    "a",
    "b",
    "c",
    "d",
    "e",
    "f",
    "g",
    "h",
    "i",
    "j",
    "k",
    "l",
    "m",
    "n",
    "o",
    "p",
    "q",
    "r",
    "s",
    "t",
    "u",
    "v",
    "w",
    "x",
    "y",
    "z",
  ];
  for (let i = 0; i < alphabet.length; i++) {
    const regex = new RegExp(alphabet[i], "i");
    if (!regex.test(s)) {
      return "not pangram";
    }
  }
  return "pangram";
}
